import math

def luas_persegi(sisi):
    return sisi * sisi

def luas_segitiga(a, t):
    return 0.5 * a * t

def luas_persegi_panjang(panjang, lebar):
    return panjang * lebar

def luas_lingkaran(r):
    return math.pi * r ** 2

def luas_jajar_genjang(alas, tinggi):
    return alas * tinggi
